export const splitItemsToPages = (numOfPage: number) => {
  const flattened = [{ id: 'ROOT' }];
  flatten(items, 'ROOT', flattened);
  const itemCount = flattened.length - 1;
  const numPerPage = getNumOfItemsPerPage(numOfPage, itemCount);

  const getAllItemsForPage = pageIndex => {
    let result = [];
    numPerPage.forEach((num, index) => {
      if (index > pageIndex) {
        return;
      }
      result = result.concat(flattened.slice(result.length, result.length + num));
    });
    return result;
  };

  const getNthPage = (pageIndex: number): ObjectShorthandCollection<TreeItemProps> => {
    const pageFlatItems = getAllItemsForPage(pageIndex);
    return reBuild(pageFlatItems);
  };

  return getNthPage;
};

export const getAllStickyHeaders = () => items.map(item => ({ ...item, items: undefined }));

const getNumOfItemsPerPage = (numOfPage: number, itemCount: number): number[] =>
  _.times(numOfPage).map(i =>
    i === numOfPage - 1
      ? (itemCount % numOfPage) + Math.floor(itemCount / numOfPage)
      : Math.floor(itemCount / numOfPage),
  );

const flatten = (items, parent, result) => {
  for (const item of items) {
    result.push({ ...item, parent });
    if (item.items) {
      flatten(item.items, item.id, result);
    }
  }
};

const reBuild = flattened => {
  const nodeMap = {};
  flattened.forEach(node => {
    const treeNode = { ...node, items: undefined, parent: undefined };
    nodeMap[treeNode.id] = treeNode;

    if (!node.parent) {
      return;
    }
    const parent = nodeMap[node.parent];
    if (!parent.items) {
      parent.items = [treeNode];
    } else {
      parent.items.push(treeNode);
    }
  });
  return nodeMap['ROOT'].items;
};

